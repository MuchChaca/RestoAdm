    private ArrayList<Command> loadOrders(){
        ArrayList<Command> listOrders = new ArrayList<Command>();
        if(CUR_TYPE.equals(ADMIN)){
            // It is an admin -> gets all commands no matter the user
            listOrders = findAll(Command.class);
            for(Command order:allOrders){
//                printCyan(order.toString());
            }
        }else{
            // It is a simple customer -> gets all commands for this user only
            //TODO: remake paramFindAll
            listOrders = paramFindAll(Command.class, "customer_id", CUR_USER.getId());
        }
        
        // Then we load the orders in the jList
        DefaultListModelCustom modLi = new DefaultListModelCustom(listOrders);
        jList1.setModel(modLi);
        
        return listOrders;
    }

