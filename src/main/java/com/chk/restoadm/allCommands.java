    private void loadOrder(Command order){
        jLabel5.setText(order.getDateOrder().toString());       // DATE ORDER
        jLabel7.setText(order.getDateDelivPlan().toString());   // DATE DELIV PLANNED
        if(order.getDateDelivAct() != null){
            jLabel9.setText(order.getDateDelivAct().toString());    // DATE ACTUAL DELIV
            jTextField3.setText("DELIVERED");
        }else{
            jTextField3.setText("PROCESSING");
        }
        jLabel11.setText(String.format("%d%n", order.getId())); // ID
        jLabel14.setText(order.getCustomer().getlName());       // LAST NAME
        jLabel15.setText(order.getCustomer().getfName());       // FIRST NAME
        jTextField2.setText(String.format("%1$,.2f", order.getTotal()));   // TTC
        
        // Load products jList
        this.allProds = new ArrayList<Product>(order.getProducts());
        DefaultListModelCustom prodLiMod = new DefaultListModelCustom(allProds);
        jList2.setModel(prodLiMod);
    }
    
